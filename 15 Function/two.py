def add(x,y):
    z = x + y
    return z

def substract(x,y):
    z = x - y
    return z

def multiply(x,y):
    z = x * y
    return z

def divid(x,y):
    z = x / y
    return z
    
def modulas(x,y):
    z = x % y
    return z
    

